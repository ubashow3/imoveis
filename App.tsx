// Upload real para Supabase Storage (CORRIGIDO)
const uploadImage = async (file: File): Promise<string | null> => {
  try {
    // 1. Limpar nome do arquivo (remove acentos e espa√ßos)
    const fileExt = file.name.split('.').pop();
    const cleanName = file.name.replace(/[^a-zA-Z0-9]/g, '');
    const fileName = `${Date.now()}_${cleanName}.${fileExt}`;
    
    console.log("Iniciando upload para:", fileName);

    // 2. Tentar Upload
    const { data, error: uploadError } = await supabase.storage
      .from('images') // O Bucket deve se chamar 'images'
      .upload(fileName, file, {
        cacheControl: '3600',
        upsert: false
      });

    if (uploadError) {
      console.error("Erro detalhado do upload:", uploadError);
      throw uploadError;
    }

    // 3. Pegar URL P√∫blica
    const { data: urlData } = supabase.storage.from('images').getPublicUrl(fileName);
    
    if (!urlData || !urlData.publicUrl) {
      throw new Error("N√£o foi poss√≠vel gerar a URL p√∫blica.");
    }

    console.log("Upload Sucesso:", urlData.publicUrl);
    return urlData.publicUrl;

  } catch (error: any) {
    console.error("FALHA NO UPLOAD:", error);
    
    let msg = error.message || "Erro desconhecido";
    
    if (msg.includes('row-level security') || msg.includes('new row violates')) {
      alert('ERRO DE PERMISS√ÉO: O Supabase bloqueou o envio.\n\nSOLU√á√ÉO: V√° em Configura√ß√µes > Ver SQL e rode o c√≥digo no Supabase para liberar o Bucket.');
    } else if (msg.includes('Bucket not found')) {
      alert('ERRO: O Bucket "images" n√£o existe.\n\nSOLU√á√ÉO: Rode o SQL de atualiza√ß√£o no Supabase.');
    } else {
      alert('Erro no upload: ' + msg);
    }
    return null;
  }
};

